from plugins.plugin import PluginInformation
from src.logger import print

PluginInfo = PluginInformation(
    name="Changelog", # This needs to match the folder name under plugins (this would mean plugins\Panel\main.py)
    description="Will show the latest changelog.",
    version="0.1",
    author="Tumppi066",
    url="https://github.com/Tumppi066/Euro-Truck-Simulator-2-Lane-Assist",
    type="static" # = Panel
)

import tkinter as tk
from tkinter import ttk
import src.helpers as helpers
import src.mainUI as mainUI
import src.variables as variables
import src.settings as settings
import os
import webview as wv

class UI():
    try: # The panel is in a try loop so that the logger can log errors if they occur
        
        def __init__(self, master) -> None:
            self.master = master # "master" is the mainUI window
            self.exampleFunction()
        
        def destroy(self):
            self.done = True
            self.root.destroy()
            del self

        
        def exampleFunction(self):
            
            try:
                self.root.destroy() # Load the UI each time this plugin is called
            except: pass
            
            self.root = tk.Canvas(self.master, width=600, height=520, border=0, highlightthickness=0)
            self.root.grid_propagate(0) # Don't fit the canvas to the widgets
            self.root.pack_propagate(0)
            
            helpers.OpenWebView("Changelog", variables.CHANGELOG[0])
            
            # Remove the tab from mainUI
            index = 1
            mainUI.UIs.pop(index)
            mainUI.pluginNotebook.forget(index)
            mainUI.pluginFrames.pop(index)
            settings.RemoveFromList("User Interface", "OpenTabs", "Changelog")
        
        
        def update(self, data): # When the panel is open this function is called each frame 
            self.root.update()
    
    
    except Exception as ex:
        print(ex.args)